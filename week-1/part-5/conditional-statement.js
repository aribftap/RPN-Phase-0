// Comparison Operators
let x = 5;

// equal to aka sama dengan
console.log(x == 5);
console.log(x == 8);
console.log(x == "5");

// equal value and equal type aka nilai yang sama dan jenis yang sama
console.log(x === 5);
console.log(x === "5");

// not equal aka tidak sama dengan
console.log(x != 6);


